CREATE TABLE IF NOT EXIST QUESTIONNAIRE (
    ID INT NOT NULL AUTO_INCREMENT,
    NAME VARCHAR(255) NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE IF NOT EXIST QUESTION (
    ID INT NOT NULL AUTO_INCREMENT,
    QUESTIONNAIRE_ID INT NOT NULL,
    QUESTION VARCHAR(255) NOT NULL,
    PRIMARY KEY (ID),
    FOREIGN KEY (QUESTIONNAIRE_ID) REFERENCES QUESTIONNAIRE(ID)
);


CREATE TABLE IF NOT EXIST REPONSE (
    ID INT NOT NULL AUTO_INCREMENT,
    QUESTION_ID INT NOT NULL,
    QUESTIONAIRE_ID INT NOT NULL
    REP VARCHAR(255) NOT NULL, -- Valeur de la r√©ponse
    TYPE_REPONSE VARCHAR(255) NOT NULL, -- TEXT, RADIO, CHECKBOX
    PRIMARY KEY (ID),
    FOREIGN KEY (QUESTION_ID) REFERENCES QUESTION(ID)
    FOREIGN KEY (QUESTIONAIRE_ID) REFERENCES QUESTIONNAIRE(ID)
);

